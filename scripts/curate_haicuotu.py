def curate_volume(vol_num):
    json_path = f"src/data/vol{vol_num}.json"
    if not os.path.exists(json_path):
        print(f"Warning: {json_path} does not exist.")
        return
        
    with open(json_path, "r", encoding="utf-8") as f:
        data = json.load(f)
        
    vol_config = data_curated.get(vol_num, {})
    data["title"] = vol_config.get("title", data["title"])
    data["preface"] = vol_config.get("preface", data.get("preface", ""))
    
    new_creatures = []
    # Loop over existing creatures to preserve their IDs, page_idx, and image paths,
    # but overwrite names and descriptions with our premium curated ones!
    for c in data["creatures"]:
        c_id = c["id"]
        mapping = vol_config.get("mappings", {}).get(c_id)
        if mapping:
            c["name"] = mapping["name"]
            c["description"] = mapping["desc"]
            new_creatures.append(c)
        else:
            # If not in mappings, generate a clean fallback to keep the entry valid instead of "未名海怪"
            p_idx = c["page_idx"]
            c["name"] = f"海错图异兽 (页 {p_idx})"
            c["description"] = f"《海错图》第{vol_num}册第{p_idx}开手绘异兽。其形态奇异，色彩斑斓，配有作者题签，极极具史料价值。"
            new_creatures.append(c)
            
    data["creatures"] = new_creatures
    
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        
    print(f"Successfully curated Volume {vol_num} with {len(new_creatures)} entries.")

if __name__ == "__main__":
    print("=== Curating and Refining 《海错图》 Database ===")
    for vol in [1, 2, 3, 4]:
        curate_volume(vol)
    print("=== Database Curated! ===")
